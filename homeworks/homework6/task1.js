function getRectangleArea(width, height) {
  return width * height;
}
console.log(getRectangleArea(5, 10));

const rectangleArea = function (width, height) {
  return width * height;
}
console.log(rectangleArea(5,7));

const rectangleAreaViaArrowFunction = (width, height) => width * height;
console.log(rectangleAreaViaArrowFunction(33, 10));
